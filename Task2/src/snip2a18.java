public class snip2a18 {

    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 20;
        // Use of conditional operators
        System.out.print("value1 == value2: ");
        System.out.println(value1 == value2);
        System.out.print("value1 != value2: ");
        System.out.println(value1 != value2);
        System.out.print("value1 > value 2: ");
        System.out.println(value1 > value2);
        System.out.print("value1 < value2: ");
        System.out.println(value1 < value2);
        System.out.print("value1 <= value2: ");
        System.out.println(value1 <= value2);
    }
}
